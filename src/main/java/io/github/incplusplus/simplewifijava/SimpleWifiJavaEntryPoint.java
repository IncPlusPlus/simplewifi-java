package io.github.incplusplus.simplewifijava;

/**
 * This class is empty and serves only as a usable reference to a class
 * contained within the simplewifi-java JAR file. To see an example of
 * this in action, see the <a href="https://github.com/IncPlusPlus/bigtoolbox-network">bigtoolbox-network</a>
 * Windows module. It uses this class to be able to extract the JavaInterop.exe file from this JAR
 * and run it.
 */
public class SimpleWifiJavaEntryPoint {
	/**
	 * This method is for testing purposes only.
	 * If you've loaded this library correctly, the string should begin with "jar:file:/"
	 * @return the string representing this class as a resource
	 */
	public static String getResourceName() {
		return String.valueOf(SimpleWifiJavaEntryPoint.class.getResource("SimpleWifiJavaEntryPoint.class"));
	}
}
